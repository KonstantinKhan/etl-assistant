package com.khan366kos.mdm.bff.partlib

object ThreadPitch {
    /**
     * Справочник шагов резьбы (по умолчанию) в зависимости от диаметра резьбы (мм).
     * Ключ – диаметр резьбы в мм (как строка), значение – шаг резьбы в мм (также как строка).
     */
    private val pitchMap = mapOf(
        "1.6" to "0.35",
        "2" to "0.40",
        "2.5" to "0.45",
        "3" to "0.50",
        "3.5" to "0.60",
        "4" to "0.70",
        "5" to "0.80",
        "6" to "1.00",
        "8" to "1.25",
        "10" to "1.50",
        "12" to "1.75",
        "14" to "2.00",
        "16" to "2.00",
        "18" to "2.50",
        "20" to "2.50",
        "22" to "2.50",
        "24" to "3.00",
        "27" to "3.00",
        "30" to "3.50",
        "36" to "4.00",
        "42" to "4.50",
        "48" to "5.00"
    )

    /**
     * Возвращает шаг резьбы (мм) по диаметру резьбы (мм) в виде строки.
     * Если диаметр не найден в справочнике, возвращает null.
     */
    fun getPitchByDiameter(diameter: String): String {
        return pitchMap[diameter] ?: "Неизвестное значение"
    }
}


